    private void okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okActionPerformed
        //esto sera horrioble papus
        String text = "";
        Figura temp = null;
        if(!fieldsAreCorrect()){
            estatus.setText("No letras solo numeros -_-");
            return;
        }
        switch (idSel) {
            case 3 -> {
                temp = new Cilindro(eNombre.getText(), Integer.parseInt(ex.getText()), Integer.parseInt(ey.getText()), Integer.parseInt(eradio.getText()), Integer.parseInt(ealtura.getText()));
                text = ("Cilindro agregado: " + eNombre.getText());
            }
            case 2 -> {
                temp = new Circulo(eNombre.getText(), Integer.parseInt(ex.getText()), Integer.parseInt(ey.getText()), Integer.parseInt(eradio.getText()));
                text = ("Circulo agregado: " + eNombre.getText());
            }
            case 1 -> {
                temp = new Punto(eNombre.getText(), Integer.parseInt(ex.getText()), Integer.parseInt(ey.getText()));
                text = ("Punto agregado: " + eNombre.getText());
            }
            default -> {
                estatus.setText("Ninguna figura seleccionada");
                emptyText();
                return;
            }
        }
        if (ctrlFig.agregarFigura(temp)) {
            estatus.setText(text);
        } else {
            estatus.setText("Figura ya existe -_-: " + eNombre.getText());
        }
        emptyText();
    }//GEN-LAST:event_okActionPerformed

