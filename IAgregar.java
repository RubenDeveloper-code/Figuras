    private boolean fieldsAreCorrect() {
        int a;
        try {
            a = Integer.parseInt(ex.getText());
            a = Integer.parseInt(ey.getText());
            a = Integer.parseInt(eradio.getText());
            a = Integer.parseInt(ealtura.getText());
        } catch (Exception e) {
            return false;
        }
        return true;
    }

